<ul>
    <li>
        <strong>Masjid Kowloon</strong>
        <p>
        Masjid ini termasuk masjid terbesar yang ada di Hong Kong karna mampu menampung sekitar 3.500 orang. Selain untuk menunaikan ibadah, Masjid Kowloon juga memiliki dua Madrasah untuk anak laki-laki dan perempuan. Selain itu, di sini juga terdapat Kelas Dakwah (Intermediate Course on Islam) untuk non-Muslim yang diselenggarakan secara rutin setiap minggu di masjid ini.
        </p>
    </li>
    <li>
        <strong>Masjid Chai Wan</strong>
        <p>
        Masjid yang dibuka pertama kali pada tahun 1963 ini merupakan masjid ke lima yang ada di Hong Kong. Kegiatan beribadah  sering diadakan di masjid ini, tidak hanya sebagai tempat beribadah saja, di Masjid Chai Wan juga terdapat Madrasah yang di khususkan untuk anak-anak yang ingin belajar Al-Quran. Selain itu masjid ini juga berada tepat di area yang sunyi dan dikelilingi oleh pemandangan hijau yang membuat ibadah para umat muslim menjadi semakin khusyuk.
        </p>
    </li>
    <li>
        <strong>Masjid Jamiah</strong>
        <p>
        Masjid Jamiah merupakan masjid pertama yang ada di Hong Kong, masjid ini dibangun pada tahun 1840-an, dianggap sebagai salah satu bangunan bersejarah oleh pemerintah Hong Kong. Seperti masjid pada umumnya, sholat lima waktu rutin diadakan di tempat ini, termasuk sholat taraweh saat bulan Ramadan. Selain itu, saat bulan ramadhan tiba, Masjid Jamia juga turut menyediakan menu sahur dan berbuka bagi umat muslim yang berpuasa.
        </p>
    </li>
</ul>


<ul>
    <li>
        <strong>Kowloon Mosque</strong>
        <p>
        This mosque is one of the largest mosques in Hong Kong because it can accommodate about 3,500 people. In addition to performing worship, the Kowloon Mosque also has two Madrasas for boys and girls. In addition, here there is also an Intermediate Course on Islam for non-Muslims which is held regularly every week at this mosque.
        </p>
    </li>
    <li>
        <strong>Chai Wan Mosque</strong>
        <p>The mosque which was first opened in 1963 is the fifth mosque in Hong Kong. Worship activities are often held at this mosque, not only as a place of worship, at Chai Wan Mosque there is also a Madrasa which is dedicated to children who want to learn the Koran. In addition, this mosque is also located right in a quiet area and surrounded by green scenery which makes the worship of Muslims more solemn.
        </p>
    </li>
    <li>
        <strong>Jamiah Mosque</strong>
        <p>
        Jamiah Mosque is the first mosque in Hong Kong, this mosque was built in the 1840s, is considered one of the historic buildings by the Hong Kong government. Like mosques in general, the five daily prayers are regularly held in this place, including the Taraweeh prayers during the month of Ramadan. In addition, when the month of Ramadan arrives, the Jamia Mosque also provides a menu for sahur and iftar for fasting Muslims.
        </p>
    </li>
</ul>